import std/[logging, os, times]

proc defaultLogPath(): string =
  let dir = getTempDir() / "moe/logs" / $getTime()
  createDir(dir)

  return dir / "moe.log"

proc initLogger*() =
  let path = defaultLogPath()

  var fileLog = newFileLogger(path, levelThreshold=lvlDebug)
  addHandler(fileLog)
